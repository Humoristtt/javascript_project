/* Сравнить null и 0. Объяснить результат */
// 0 - это числовое значение, а null указывает на отсутствие какого-либо значения.

console.log(null == 0);
console.log(null > 0);
console.log(null < 0);
console.log(null <= 0);
console.log(null >= 0);

